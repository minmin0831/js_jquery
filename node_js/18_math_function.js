// round, floor, ceil 반월림, 내림, 올림 함수

const maxResult = Math.max(56, 78, 20, 100, 50);
const minResult = Math.min(56, 78, 20, 100, 50);
console.log('max : ', maxResult);
console.log('min : ', minResult);
const nums = [51, 78, 36, 87, 30];
console.log('max : ', Math.max(nums));
console.log('절대값 : ', Math.abs(-3));
console.log('거듭제곱 : ', Math.pow(5, 3));
console.log('제곱근 : ', Math.sqrt(2));
console.log('부호함수 : ', Math.sign(50));

console.log(Math.trunc(23.445)); // truncate: 소수점을 모두 제거.
